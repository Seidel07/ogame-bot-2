package model;

public class Constructions {
	
	private String buildName;
	private Integer metalRequired;
	private Integer crystalRequired;
	private Integer deuteriumRequired;
	private Integer level;
	private Integer orderInElementList;
	
	public String getBuildName() {
		return buildName;
	}
	public void setBuildName(String buildName) {
		this.buildName = buildName;
	}
	public Integer getMetalRequired() {
		return metalRequired;
	}
	public void setMetalRequired(Integer metalRequired) {
		this.metalRequired = metalRequired;
	}
	public Integer getCrystalRequired() {
		return crystalRequired;
	}
	public void setCrystalRequired(Integer crystalRequired) {
		this.crystalRequired = crystalRequired;
	}
	public Integer getDeuteriumRequired() {
		return deuteriumRequired;
	}
	public void setDeuteriumRequired(Integer deuteriumRequired) {
		this.deuteriumRequired = deuteriumRequired;
	}
	public Integer getLevel() {
		return level;
	}
	public void setLevel(Integer level) {
		this.level = level;
	}
	public Integer getOrderInElementList() {
		return orderInElementList;
	}
	public void setOrderInElementList(Integer orderInElementList) {
		this.orderInElementList = orderInElementList;
	}

}
